    {
        $data = array();
        if ($object->isInitialized('name') && null !== $object->getName()) {
            $data['name'] = $object->getName();
        }
        if ($object->isInitialized('description') && null !== $object->getDescription()) {
            $data['description'] = $object->getDescription();
        }
        if ($object->isInitialized('deviceCount') && null !== $object->getDeviceCount()) {
            $data['deviceCount'] = $object->getDeviceCount();
        }
        if ($object->isInitialized('quarantined') && null !== $object->getQuarantined()) {
            $data['quarantined'] = $object->getQuarantined();
        }
        foreach ($object as $key => $value) {
            if (preg_match('/.*/', (string) $key)) {
                $data[$key] = $value;
            }
        }
        return $data;
    }
