    {
        if (isset($data['$ref'])) {
            return new Reference($data['$ref'], $context['document-origin']);
        }
        if (isset($data['$recursiveRef'])) {
            return new Reference($data['$recursiveRef'], $context['document-origin']);
        }
        $object = new \Mobility\Model\DeviceGroup();
        if (\array_key_exists('deviceCount', $data) && \is_int($data['deviceCount'])) {
            $data['deviceCount'] = (double) $data['deviceCount'];
        }
        if (null === $data || false === \is_array($data)) {
            return $object;
        }
        if (\array_key_exists('name', $data)) {
            $object->setName($data['name']);
            unset($data['name']);
        }
        if (\array_key_exists('description', $data)) {
            $object->setDescription($data['description']);
            unset($data['description']);
        }
        if (\array_key_exists('deviceCount', $data)) {
            $object->setDeviceCount($data['deviceCount']);
            unset($data['deviceCount']);
        }
        if (\array_key_exists('quarantined', $data)) {
            $object->setQuarantined($data['quarantined']);
            unset($data['quarantined']);
        }
